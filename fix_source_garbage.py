import os

def fix_file(file_path):
    with open(file_path, 'rb') as f:
        content = f.read()

    # Map of bytes to bytes replacements
    # We use bytes to be safe from encoding issues during the fix process itself.
    replacements = [
        (b'\xe9\x9a\x90\xe8\x97\x8f\xe5\x88\x97\xef\xbf\xbd', b'\xe9\x9a\x90\xe8\x97\x8f\xe5\x88\x97'), # 隐藏列
        (b'\xf0\x9f\x91\x81\xef\xbf\xbd?', b'\xf0\x9f\x91\x81 '), # 👁
        (b'\xe2\x9a\x99\xef\xb8\x8f \xe5\x88\x97\xe8\xae\xbe\xef\xbf\xbd?', b'\xe2\x9a\x99\xef\xb8\x8f \xe5\x88\x97\xe8\xae\xbe\xe7\xbd\xae'), # ⚙️ 列设置
        (b'\xe5\xba\x94\xe7\x94\xa8\xe7\xad\x9b\xef\xbf\xbd', b'\xe5\xba\x94\xe7\x94\xa8\xe7\xad\x9b\xe9\x80\x89'), # 应用筛选
        (b'\xe6\xb8\x85\xe9\x99\xa4\xe6\xad\xa4\xe5\x88\x97\xe7\xad\x9b\xef\xbf\xbd', b'\xe6\xb8\x85\xe9\x99\xa4\xe6\xad\xa4\xe5\x88\x97\xe7\xad\x9b\xe9\x80\x89'), # 清除此列筛选
        (b'\xe6\x97\xa5\xef\xbf\xbd?', b'\xe6\x97\xa5\xe5\xbf\x97'), # 日志
        (b'\xe7\xbc\x96\xe7\xa0\x81\xef\xbf\xbd?', b'\xe7\xbc\x96\xe7\xa0\x81'), # 编码
        (b'\xe5\xb7\xa5\xef\xbf\xbd?', b'\xe5\xb7\xa5\xe5\x85\xb7'), # 工具
        (b'\xe6\x97\xa0\xe6\x95\xb0\xef\xbf\xbd', b'\xe6\x97\xa0\xe6\x95\xb0\xe6\x8d\xae'), # 无数据
        (b'\xe6\x97\xa0\xe6\xb3\x95\xe5\xaf\xbc\xef\xbf\xbd', b'\xe6\x97\xa0\xe6\xb3\x95\xe5\xaf\xbc\xe5\x87\xba'), # 无法导出
        (b'\xe6\x9c\xaa\xe5\xae\x89\xef\xbf\xbd?', b'\xe6\x9c\xaa\xe5\xae\x89\xe8\xa3\x85 '), # 未安装
        (b'\xe5\xbe\x85\xe7\xa1\xae\xef\xbf\xbd', b'\xe5\xbe\x85\xe7\xa1\xae\xe8\xae\xa4'), # 待确认
        (b'\xe5\xb7\xb2\xe6\xa0\xb8\xef\xbf\xbd', b'\xe5\xb7\xb2\xe6\xa0\xb8\xe5\xae\x9e'), # 已核实
        (b'\xe6\x96\xb0\xe6\xa0\x87\xe7\xad\xbe\xe5\x90\x8d\xef\xbf\xbd', b'\xe6\x96\xb0\xe6\xa0\x87\xe7\xad\xbe\xe5\x90\x8d\xe7\xa7\xb0'), # 新标签名称
        (b'\xe6\x80\xa7\xe6\x90\x9c\xe7\xb4\xa2\xe8\xae\xbe\xef\xbf\xbd', b'\xe6\x80\xa7\xe6\x90\x9c\xe7\xb4\xa2\xe8\xae\xbe\xe7\xbd\xae'), # 性搜索设置
        (b'(Key) \xef\xbf\xbd?', b'(Key) \xe4\xb8\x8e '), # 与
        (b'\xe4\xbb\x85\xe5\x8c\xb9\xef\xbf\xbd?', b'\xe4\xbb\x85\xe5\x8c\xb9\xe9\x85\x8d '), # 仅匹配
        (b'\xef\xbf\xbd?', b' '), # Generic fallback for remaining single replacement chars
        (b'\xef\xbf\xbd', b''), # Remove remaining garbage
    ]

    new_content = content
    for old, new in replacements:
        new_content = new_content.replace(old, new)

    if new_content != content:
        with open(file_path, 'wb') as f:
            f.write(new_content)
        print(f"Fixed {file_path}")
    else:
        print(f"No changes for {file_path}")

if __name__ == "__main__":
    fix_file('brazil_product_code_v1.02.py')
